    private void addNameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNameBtnActionPerformed
       
        DefaultTreeModel model = (DefaultTreeModel) familyMenu.getModel();
        DefaultMutableTreeNode root = (DefaultMutableTreeNode) model.getRoot();
        
        if (addNameTf.getText().trim().equals ("")) {
	root.add(new DefaultMutableTreeNode (addNameTf.getText()));
	model.reload();
	
} else {
	lMessage.setText("You Must Enter A Name");
}
    }//GEN-LAST:event_addNameBtnActionPerformed

