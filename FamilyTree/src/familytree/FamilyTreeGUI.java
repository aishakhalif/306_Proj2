    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
       lMessage.setText("");
       DefaultTreeModel model = (DefaultTreeModel) familyMenu.getModel();
       DefaultTreeModel selectedNode = (DefaultTreeModel) familyMenu.getLastSelectedPathComponent();
       
       if (selectedNode.isLeaf(model)) {
       
           model.removeNodeFromParent((MutableTreeNode) selectedNode);
           
           
       } else {
       
		if (selectedNode!=null) {
		
		lMessage.setText ("Please select something to delete");
                }
       }
    
    }//GEN-LAST:event_deleteBtnActionPerformed

